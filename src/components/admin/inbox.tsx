"use client";
import { useEffect, useState } from "react";
import { Button } from "@/components/ui/button";
import { Card, CardContent } from "@/components/ui/card";
import {
  Dialog,
  DialogContent,
  DialogTrigger,
  DialogHeader,
  DialogTitle,
  DialogDescription,
} from "@/components/ui/dialog";
import { Input } from "@/components/ui/input";
import { useToast } from "@/components/ui/use-toast";
import { CopyButton } from "../copyBtn";
import { DisplayMessage } from "./displayMessage";

type posterNameType = {
  name: string;
};
type claimedNameType = {
  seller: {
    name: string;
  };
} | null;

type Post = {
  id: string;
  phone: string;
  message: string;
  clientName: string;
  claimedAt: string;
  createdAt: string;
  poster: posterNameType;
  claim: claimedNameType;
  new: boolean;
};

type Pagination = {
  page: number;
  limit: number;
  totalPages: number;
  totalClaims: number;
};

const AdminInbox = () => {
  const [posts, setPosts] = useState<Post[]>([]);
  const [page, setPage] = useState(1);
  const [pagination, setPagination] = useState<Pagination | null>(null);
  const [selectedPost, setSelectedPost] = useState<Post | null>(null);
  const [search, setSearch] = useState("");
  const { toast } = useToast();

  // Fetch paginated claims (no search)
  const fetchLeads = async (pageToLoad = 1, append = false) => {
    try {
      const res = await fetch(`/api/admin/leads?page=${pageToLoad}`);
      const data = await res.json();

      const nextPosts = Array.isArray(data) ? data : data.posts || [];
      if (append) {
        setPosts((prev) => [...prev, ...nextPosts]);
      } else {
        setPosts(nextPosts);
      }

      setPagination((Array.isArray(data) ? null : data.pagination) || null);
    } catch (err) {
      console.error("Failed to fetch claims", err);
    }
  };

  // Fetch search results (no pagination; backend limits to 10)
  const fetchSearchLeads = async (query: string) => {
    try {
      const res = await fetch(
        `/api/admin/leads/search?phone=${encodeURIComponent(query)}`
      );
      const data = await res.json();
      // The search API returns an array directly
      setPosts(Array.isArray(data) ? data : []);
      setPagination(null); // disable "Load More" while searching
      setPage(1);
    } catch (err) {
      console.error("Failed to fetch search claims", err);
    }
  };

  const handleLoadMore = () => {
    if (pagination && page < pagination.totalPages) {
      const nextPage = page + 1;
      setPage(nextPage);
      fetchLeads(nextPage, true);
    }
  };

  // Initial load
  useEffect(() => {
    fetchLeads();
  }, []);

  // Debounced search
  useEffect(() => {
    const q = search.trim();
    const t = setTimeout(() => {
      if (q) {
        fetchSearchLeads(q);
      } else {
        // go back to paginated list
        setPage(1);
        fetchLeads(1);
      }
    }, 400);
    return () => clearTimeout(t);
  }, [search]);

  return (
    <div className="space-y-6 max-w-3xl mx-auto p-4">
      {/* Search Input */}
      <Input
        placeholder="Search by phone number..."
        value={search}
        onChange={(e) => setSearch(e.target.value)}
        className="max-w-full"
      />

      {/* Posts List */}
      <div className="space-y-4">
        {posts.map((post) => (
          <Dialog key={post.id}>
            <DialogTrigger asChild>
              <Card
                onClick={() => {
                  setSelectedPost(post);
                }}
                className="cursor-pointer hover:shadow-md transition py-2"
              >
                <CardContent className="px-4 py-2 space-y-1">
                  <div className="flex justify-between items-center">
                    <p className="text-sm text-gray-600">Phone: {post.phone}</p>
                    <div className="flex gap-2 items-center">
                      <p>{new Date(post.createdAt).toLocaleString()}</p>
                      {post.new && (
                        <p className="bg-emerald-600 rounded-full px-2 text-sm text-white">
                          New
                        </p>
                      )}
                    </div>
                  </div>
                  <p className="text-gray-800 line-clamp-2">{post.message}</p>
                </CardContent>
              </Card>
            </DialogTrigger>
            <DialogContent>
              <DialogHeader>
                <DialogTitle>Post Details</DialogTitle>
                <DialogDescription className="hidden">
                  View the full message and phone number of this post.
                </DialogDescription>
              </DialogHeader>
              <div className="space-y-4 mt-2">
                <div>
                  <p className="text-sm font-medium text-gray-500">
                    Generated By
                  </p>
                  <p className="whitespace-pre-wrap">
                    {selectedPost?.poster.name}
                  </p>
                </div>
                {selectedPost?.claim && (
                  <div>
                    <p className="text-sm font-medium text-gray-500">
                      Claimed By
                    </p>
                    <p className="whitespace-pre-wrap">
                      {selectedPost?.claim.seller.name}
                    </p>
                  </div>
                )}
                <div>
                  <p className="text-sm font-medium text-gray-500">
                    Phone Number
                  </p>
                  <div className="flex justify-between items-center gap-3">
                    <p className="text-lg">{selectedPost?.phone}</p>
                    {selectedPost?.phone && (
                      <CopyButton
                        text={selectedPost.phone}
                        className="cursor-pointer"
                      />
                    )}
                  </div>
                </div>
                <div>
                  <p className="text-sm font-medium text-gray-500">Name</p>
                  <p className="text-lg">{selectedPost?.clientName}</p>
                </div>
                <DisplayMessage
                  type="Details"
                  message={selectedPost?.message}
                />
              </div>
            </DialogContent>
          </Dialog>
        ))}

        {/* Load More Button (hidden during search) */}
        {!search.trim() && pagination && page < pagination.totalPages && (
          <div className="flex justify-center pt-4">
            <Button variant="outline" onClick={handleLoadMore}>
              Load More
            </Button>
          </div>
        )}

        {/* Empty State */}
        {posts.length === 0 && (
          <p className="text-center text-sm text-muted-foreground">
            {search.trim()
              ? "No results for that phone number."
              : "No Leads Found."}
          </p>
        )}
      </div>
    </div>
  );
};

export default AdminInbox;
